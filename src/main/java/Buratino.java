public class Buratino {
    public static void main(String[] args) {
        System.out.println("буратино и яблоки");

        byte buratino = 4;
        System.out.println("количество яблок у буратино "+buratino);

        byte barobas = 2;
        System.out.println("количество яблок которые забрал баробас "+barobas);

        int res = buratino - barobas;
        System.out.println("количество яблок которое осталось у буратино "+res);

    }
}
